#include <unistd.h>

int main(int argc, char **argv)
{
	int i;
	int j;


	i = 0;
	if (argc == 2)
	{
		while (argv[1][i] != '\0')
			i++;
		i--;
		while (i != 0 && (argv[1][i] == 32 || (argv[1][i] >= 9 && argv[1][i] <= 13)))
			i--;
		while (i != 0 && (argv[1][i - 1] < 9 || argv[1][i - 1] > 13) && argv[1][i - 1] != 32)
			i--;
		while (argv[1][i] != '\0' && (argv[1][i] < 9 || argv[1][i] > 13) && argv[1][i] != ' ')
		{
			write(1, &argv[1][i], 1);
			i++;	
		}
	}

	write(1, "\n", 1);
	return (0);
}	
